// (a) Function with No Return Type and No Parameters
#include <stdio.h>

// This program demonstrates a function with no return type and no parameters.
// It simply prints a message to the console.
// Function Declaration (Prototype)
void greet(); // Function prototype

// Function Definition
void greet() { // Function definition
    printf("Hello, World!\n");
}
// Main Function
int main() {
    // Function Call
    greet(); // Call the function
    return 0;
}
// Output:
// Hello, World!
